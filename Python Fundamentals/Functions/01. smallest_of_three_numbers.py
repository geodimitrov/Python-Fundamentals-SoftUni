# Define the function, use min function
def smallest_of_three_nums(num_1, num_2, num_3):
    return (min(num_1, num_2, num_3))

# Introduce the numbers
num_one = int(input())
num_two = int(input())
num_three = int(input())

#print result
print(smallest_of_three_nums(num_one, num_two, num_three))


